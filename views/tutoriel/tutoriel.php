<h2>Tutoriel d'installation et infos diverses</h2>
<h3>Installation</h3>
<div>
	Pour vous connecter et discuter avec vos amis, il vous faut "mumble"... Il est assez simple à installer :
	<ul>
		<li>Téléchargez la version correspondant à votre plateforme sur cette <a href="http://mumble.sourceforge.net/">page</a>,</li>
		<li>Double cliquez sur l'installeur,</li>
		<li>Acceptez les conditions d'utilisations,</li>
		<li>Dans le choix des options à installer, vérifiez que vous avez coché mumble et décoché murmur,</li>
		<li>Suivant, installer...</li>
	</ul>
</div>
<div>
	Un fois mumble installé, il vous faudra dans certains cas redémarrer votre machine. Faites-le avant de continuer.
</div>
<h3>Configuration</h3>
<div>
	Vous avez fini d'installer mumble, vous voici prêt à le configurer.
	Lors du premier lancement du logiciel, l'assistant audio vous guidera pour faire cela au mieux...
</div>
<div>
	<strong>Remarque importante</strong>, il vous est fortement recommandé d'utiliser l'option <em>"push-to-talk"</em> de mumble.
	Si vous êtes dans un environnement calme, vous pouvez utiliser l'activité vocale,
	de cette manière, mumble détectera quand vous parlez pour ne transmettre qu'à ces moments.
</div>
<div>
	Tout rapport justifié de quelqu'un utilisant la <em>"transmission continue"</em>
	sera suivi de sanction allant de la <strong>remarque</strong> au <strong>ban !!!</strong>
</div>
<h3>Connexion</h3>
<div>
	Une fois votre logiciel configuré, il y a deux façons de vous connecter au serveur :
	<ul>
		<li>Cliquer <a href="<?php echo $url ?>">ici</a>.</li>
		<li>Ou si votre navigateur ne gère pas ce type de lien :
			<ul>
				<li>Cliquez dans la fenêtre mumble sur le menu <em>"serveur"</em>,</li>
				<li>Cliquez sur l'entrée de menu <em>"connexion"</em>,</li>
				<li>Dans la fenêtre qui s'ouvre, cliquez sur le bouton <em>"Ajouter nouveau..."</em></li>
				<li>Entrez les informations de connexion disponible sur cette <a href="<?php echo url('informations') ?>">page</a>.</li>
				<li>Choississez un nom pour le serveur et un nom d'utilisateur,</li>
				<li>Vous pouvez vous connecter !</li>
			</ul>
		</li>
	</ul>
</div>
<h3>L'organisation du serveur</h3>
<div>
	Le serveur est divisée en plusieurs parties :
	<ul>
		<li>Le salon <em>"Public"</em></li>
		<li>Les salons <em>"sur demande"</em></li>
		<li>Le salon <em>"Private"</em></li>
	</ul>
	<div>
		<h4>Le salon Public</h4>
		<p>
			Par défaut, vous vous connectez dans ce salon, il est accessible par tous et
			vous n'avez accès qu'à lui. Si vous ne souhaitez pas rester dans ce salon,
			plusieurs choix s'offrent à vous :
		</p>
		<ul>
			<li>Créer un salon <em>"temporaire"</em> (clic droit ajouter sur le salon public), celui-ci disparaitra dès que vous le quitterez...</li>
			<li>
				Demander à un admin (s'il y en a en ligne) ou envoyer un message via le <a href="<?php echo url('contact') ?>">formulaire de contact</a>
				pour que l'on vous crée un salon <em>"permanent"</em> public ou protégé par mot de passe.
			</li>
		</ul>
		<p>
			Le salon public est une faveur faite à tout un chacun ! Si jamais c'est le boxon,
			nous nous réservons le droit de kick/ban les gens à l'envie,
			voir de le réserver à ceux qui en feront la demande depuis le site (et qui seront accepté).
		</p>
	</div>
	<div>
		<h4>Les salons sur demande</h4>
		<p>
			Les salons sur demande sont accordés sur demande évidemment.
