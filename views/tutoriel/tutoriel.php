		</p>
		<p>
			Si vous en faite partie, il vous suffit de demander à l'admin du salon de vous donner la permission
			d'y entrer.
		</p>
		<p>
			Si vous êtes admin de salon et que vous avez un problème pour configurer les droits de votre salon,
